  data: function () {
    return {
      apis: {},
    };
  },
  methods: {
    getData: function (params) {
      fetch("https://api.publicapis.org/entries")
        .then((response) => response.json())
        .then((data) => (this.apis = data));
    },
  },
  mounted: function () {
    this.$nextTick(() => {
      this.getData();
    });
  },
};
